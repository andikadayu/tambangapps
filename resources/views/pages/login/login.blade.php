        function loginaction(){
            $.ajax({
                url:"{{route('login_process')}}",
                method:'POST',
                processData:false,
                contentType:false,
                type:'post',
                headers: {
                    'x-csrf-token': $('meta[name="csrf-token"]').attr('content')
                },
                data: new FormData($('#formLogin')[0]),
                dataType:'json',
                success:function (response){
                    if(response.status){
                        Swal.fire('Success',response.msg,'success');
                        location.reload();
                    }else{
                        Swal.fire(
                            'Error',
                            response.msg,
                            'error'
                        );
                    }
                }
            })
        }
